<h1>Socket Test Application with Circle and circle-stdlib</h1>
<p>This is a SvelteKit web application that is served by a Mongoose web server that is embedded in the Circle application.</p>
<p>There are two demo pages:</p>
<ul>
    <li><a href="/examples/rest-api">REST API Example</a>: Fetches system status data from a REST API endpoint provided by the Circle application.</li>
    <li><a href="/examples/websocket">WebSocket Chat Example</a>: Demonstrates real-time communication using WebSockets with a simple chat interface.</li>
</ul>
